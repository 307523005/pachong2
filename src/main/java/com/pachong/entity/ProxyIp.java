package com.pachong.entity;

public class ProxyIp {
	private String	pi_id;
	private String	pi_ip;
	private String	pi_port;
	private String	pi_time;
	
	public String getPi_time() {
		return pi_time;
	}
	public void setPi_time(String pi_time) {
		this.pi_time = pi_time;
	}
	public String getPi_id() {
		return pi_id;
	}
	public void setPi_id(String pi_id) {
		this.pi_id = pi_id;
	}
	public String getPi_ip() {
		return pi_ip;
	}
	public void setPi_ip(String pi_ip) {
		this.pi_ip = pi_ip;
	}
	public String getPi_port() {
		return pi_port;
	}
	public void setPi_port(String pi_port) {
		this.pi_port = pi_port;
	}
	
}
